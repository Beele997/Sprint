 #define RESET_BIT(REG,BIT_NUM) REG&=(~(1<<BIT_NUM))
 #define TOGGLE(REG,BIT_NUM) (REG^=(1<<BIT_NUM))
 #define GET_BIT(REG,BIT_NUM) ((REG>>BIT_NUM)&1)


 
 
